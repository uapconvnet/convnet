namespace CustomMessageBox.Avalonia;

/// <summary>
/// Specifies the icon to display in a message box.
/// </summary>
public enum MessageBoxIcon
{
	/// <summary>
	/// No icon.
	/// </summary>
	None,

	/// <summary>
	/// A question icon.
	/// </summary>
	Question,

	/// <summary>
	/// An error icon.
	/// </summary>
	Error,

	/// <summary>
	/// A warning icon.
	/// </summary>
	Warning,

	/// <summary>
	/// An information icon.
	/// </summary>
	Information
}
